// while loop
// print 1 to 10
let count = 1;
while (count <= 10) {
    // console.log(count);
    count++;
}

// use cases for while loop: when number of iterations are not known in advance
// 1. infinite scrolling: zomato, swiggy, facebook
// 2. wait for page loading: 2, 5, 10, 15 seconds
// 3. wait for the element to be visible on page
// 4. calendar handling: select date, month, year
// 5. web table pagination: next, previous, first, last

// use cases for loop:
// 1. when number of iterations is known in advance
// 2. dropdown: month: Jan to Dec: 1 to 12

// do - while loop:
// print 1 to 10
let countDoWhile = 1;
do {
    console.log(countDoWhile);
    countDoWhile++;
} while (countDoWhile <= 10);